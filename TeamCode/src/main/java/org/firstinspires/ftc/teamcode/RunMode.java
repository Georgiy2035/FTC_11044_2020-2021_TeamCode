package org.firstinspires.ftc.teamcode;

public class RunMode {
    public double speed, minX, minY, minA;
    public RunMode (double speed, double minX, double minY, double minA) {
        this.speed = speed;
        this.minX = minX;
        this.minY = minY;
        this.minA = minA;
    }
}
